	argstostr := func(args []reflect.Value) (str string) {
		for _, a := range args {
			str += fmt.Sprintf("%v, ", a.Interface())
		}
		if len(str) > 1 {
			str = str[:len(str)-2] // drop last ", "
		}
		return "[" + str + "]"
	}

